    public void cargar(){
        
        
                    try {
                        DefaultTableModel modelo= new DefaultTableModel();
                        jtDetalle.setModel(modelo);

                        String SQL = "SELECT * FROM ventas" ;
                        pps= cn.prepareStatement(SQL);
                        rs=pps.executeQuery();

                        java.sql.ResultSetMetaData rsMD = rs.getMetaData(); 
                        int cantidadColumnas = rsMD.getColumnCount(); 

                        modelo.addColumn("Id Venta");
                        modelo.addColumn("Fecha");
                        modelo.addColumn("Id Empleado");
                        modelo.addColumn("Subtotal");
                        modelo.addColumn("Impuesto");
                        modelo.addColumn("Descuento");
                        modelo.addColumn("Total");
                        
                        

                        while (rs.next()){
                            Object[] filas = new Object[cantidadColumnas];
                            for (int i=0;i<cantidadColumnas; i++)
                            {
                                filas[i]= rs.getObject(i+1);
                            }
                            modelo.addRow(filas);
                        }
                    } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(null,"Error" + ex);
                    }   
    }
    
    
    
    
    
     public void Add(){           
        
        try {
                pps = cn.prepareStatement("INSERT INTO ventas(fechaventa, idempleado, subtotal, impuesto, descuento, total) VALUES (? ,?, ?, ?, ?, ?)"); 
               
                pps.setString(1, txtFecha.getText());
                pps.setInt(2, cmbEmpleado.getSelectedIndex());
                pps.setDouble(3, Double.parseDouble(txtSubtotal.getText()));
                pps.setDouble(4, Double.parseDouble(txtISV.getText()));
                pps.setDouble(5, Double.parseDouble(txtDesc.getText()));
                pps.setDouble(6, Double.parseDouble(txtTotal.getText()));
                
                pps.executeUpdate();

            } catch (SQLException ex) {
                java.util.logging.Logger.getLogger(Cliente.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    
    
